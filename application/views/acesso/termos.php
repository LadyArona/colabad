<br>O PORTAL SIMI
<br>
<br>Aberto a toda a sociedade, o portal é uma ferramenta estratégica que reúne em um só local as principais informações sobre empreendedorismo e inovação.
<br>
<br> 
<br>
<br>1- CADASTRO DE USUÁRIO
<br>
<br>São considerados USUÁRIOS pessoas naturais ou jurídicas, prévia e regularmente, cadastradas no PORTAL SIMI, conforme determinado neste documento: USUÁRIO COMUM, USUÁRIO INSTITUIÇÃO, USUÁRIO PARCEIRO E USUÁRIO COLUNISTA.
<br>
<br>O cadastrado de usuário pode ser desativado em qualquer momento. Porém, todos as informações relacionadas a conteúdo, que foram aprovados e publicados no portal, ficarão no ar por tempo indeterminado.
<br>
<br>
<br>1.1 USUÁRIO COMUM
<br>
<br>Para tornar-se um USUÁRIO COMUM do PORTAL SIMI é necessário realizar um cadastro no site. Após oferecer as informações básicas e confirmar, o mesmo receberá um e-mail de confirmação para validar o cadastro.
<br>
<br>
<br>1.2 USUÁRIO INSTITUIÇÃO
<br>
<br>O USUÁRIO INSTITUIÇÃO do PORTAL SIMI, passa, incialmente pelo mesmo processo de cadastro do USUÁRIO COMUM, porém, o cadastro passa por uma aprovação do administrador do portal. São aprovadas instituições que têm relação às áreas de ciência, tecnologia, inovação ou empreendedorismo.
<br>
<br>1.2.1 Mapa da Inovação
<br>
<br>Serão aprovadas no Mapa da Inovação as instituições previamente aprovadas pelo administrador do PORTAL SIMI e que têm relação às áreas de ciência, tecnologia, inovação ou empreendedorismo.
<br>
<br>
<br>1.3 USUÁRIO PARCEIRO
<br>
<br>O USUÁRIO PARCEIRO é aquele responsável por uma instituição parceira do PORTAL SIMI. Para tornar-se parceiro é necessário que a instituição tenha relação com áreas de ciência, tecnologia, inovação ou empreendedorismo.
<br>
<br>Para informações de como tornar-se um parceiro, envie um formulário através do Fale Conosco.
<br>
<br>
<br>1.4 USUÁRIO COLUNISTA
<br>
<br>O USUÁRIO COLUNISTA precisa aprovação do PORTAL SIMI.
<br>
<br>Para informações de como se tornar colunista, envie um formulário através do Fale Conosco.
